package es.mde.entidades;

public class Equipo {
	private String nombre;
	private boolean mixto;

	public Equipo() {
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public boolean isMixto() {
		return mixto;
	}

	public void setMixto(boolean mixto) {
		this.mixto = mixto;
	}

}
